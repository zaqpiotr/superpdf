package superpdf;

/**
 * Text styling types for annotations and text decorations.
 * Defines various text style options that can be applied to text content.
 */
public enum TextType {
    /** Text highlighting style */
    HIGHLIGHT,
    /** Text underlining style */
    UNDERLINE,
    /** Squiggly underline style */
    SQUIGGLY,
    /** Text strikethrough style */
    STRIKEOUT;
}
